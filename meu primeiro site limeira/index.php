<h3>Prefeito visita Centro Comunitário Ouro Verde e acompanha atividades após reforma 18/04/2019</h3>
                <img src="fotos/prefe.jpg" height="280" />
<p>O prefeito Mario Botion visitou, nesta quinta-feira (18), o Centro Comunitário “Professor Sebastião Fumagalli”, no Jardim Ouro Verde. O espaço, que oferece atividades esportivas e atendimento socioassistencial, passou por ampla reforma e revitalização e já pode ser usado pela comunidade desde o começo do mês. Vereadores, secretários municipais, servidores e usuários também estiveram no local para acompanhar a visita. Os investimentos para a reforma são da ordem de R$ 200 mil, obtidos por meio de emenda parlamentar do deputado federal Miguel Lombardi.

A reforma desse espaço faz parte de um conjunto de obras, previstas no programa Limeira Ativa e Saudável, lançado no ano passado. O prefeito destacou que o local atende uma região de 74 bairros e que alguns problemas, adquiridos com o tempo e por atos de vandalismo, levaram à necessidade de revitalização do centro comunitário. “Fizemos uma reforma significativa em toda a estrutura e melhorias em vários serviços que são prestados aqui. Agora, entregamos para a comunidade, para que ela continue usando e cuidando do espaço”, falou.

O secretário de Esporte e Lazer, Luiz Augusto Zanon, comenta que a principal melhoria foi na cobertura, com a recuperação e pintura de toda a estrutura metálica. “Também fizemos melhorias nos vestiários e outras pontuais. A maior foi na parte estrutural, na iluminação na quadra e no entorno da piscina. Agora, colocamos a disposição da comunidade o espaço revitalizado”, destacou. Segundo Zanon, o local atende todos os públicos, de diversas idades. “É um centro comunitário muito movimentado e temos certeza que a população vai gostar muito das benfeitorias realizadas neste lugar.”

Botion lembrou que, além das atividades esportivas, o próprio público passou a ser o primeiro polo descentralizado do Ceprosom, com atendimento socioassistencial. “O Ceprosom tem profissionais aqui que prestam serviços complementares àqueles que são prestados na sede da autarquia. Isso tem a finalidade de trazer o atendimento mais próximo da comunidade, em um processo contínuo de descentralização”, falou.

Entre os serviços oferecidos no local, estão a atualização do Cadastro Único, Serviço de Proteção e Atendimento Integral à Família (PAIF), acompanhamento do Bolsa Família, Serviço de Convivência e Fortalecimento de Vínculos para crianças, adolescentes e idosos, e atendimento a domicílio para a terceira idade e pessoas com deficiência. Além disso, também haverá atendimento do programa Criança Feliz, a partir da semana que vem.

O vereador e presidente da Câmara Municipal, Sidney Pascotto, o Lemão da Jeová Rafá, enalteceu o trabalho do deputado federal Miguel Lombardi na conquista da verba para a revitalização do centro comunitário. Ele também parabenizou o prefeito Mario Botion pelo empenho na reforma de espaços esportivos e de saúde. No dia 27, a revitalização da praça de esportes do Jardim Piratininga também será entregue. Além disso, as obras da Praça de Esportes do Jardim Santa Eulália seguem avançadas.

Acompanharam o prefeito durante a visita a presidente do Ceprosom, Maria Aucélia Damaceno, os secretários Edison Moreno Gil (chefe de Gabinete), Dagoberto Guidi (Obras e Serviços Públicos), Francisco Alves (Segurança Pública e Defesa Civil), Antonio Peres (Comunicação Social), além de Ademir Cintra, representando o deputado federal Miguel Lombardi, e dos vereadores Erika Tank, Wagner Barbosa, Claudemir Vieira (Mir do Lanche) e Estevão Nogueira. </p>
